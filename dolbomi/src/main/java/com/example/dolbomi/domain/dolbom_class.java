package com.example.dolbomi.domain;

public class dolbom_class {
    private Long id;
    private String class_name;
    private Long class_num;
    private String year_seme;
    private Long disable;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getClass_name() {
        return class_name;
    }

    public void setClass_name(String class_name) {
        this.class_name = class_name;
    }

    public Long getClass_num() {
        return class_num;
    }

    public void setClass_num(Long class_num) {
        this.class_num = class_num;
    }

    public String getYear_seme() {
        return year_seme;
    }

    public void setYear_seme(String year_seme) {
        this.year_seme = year_seme;
    }

    public Long getDisable() {
        return disable;
    }

    public void setDisable(Long disable) {
        this.disable = disable;
    }
}
